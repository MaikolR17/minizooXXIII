                <p id="description">El dorso es de color pardo amarillento a marrón oscuro, casi negro, mientras que la zona ventral puede ser blanquecina o anaranjada fuerte, el pelo es espeso y opaco. La cabeza es triangular y el hocico largo, la cola es marrón con anillos amarillentos característicos, generalmente la mantiene levantada en forma perpendicular al cuerpo. Posee orejas pequeñas y redondeadas con pelos blancos en el borde interior.\r\nLas extremidades son oscuras con uñas largas y fuertes. El macho es más robusto que la hembra y posee los caninos más desarrollados. El largo del cuerpo es de 410 a 670 mm el largo de la cola es de 320 a 690 mm y pesa 3,5 a 6 Kg.</p>
                <h3>ECOLOGÍA</h3>
                <p id="ecology">Se encuentra en diferentes tipos de vegetación, desde bosque espinoso achaparrado hasta bosque húmedo tropical. Es omnívora, come frutas y es depredadora oportunista. Es diurna, terrestre, trepadora y arborícola, la hembra forma grupos de hasta 30 individuos y se desplaza formando tropas, pero el macho es a menudo solitario. Las crías nacen después de los 77 días de gestación en un nido construido en los árboles por la hembra. La camada varia de una a seis crías. Durante la primera etapa de vida, las crías permanecen en el nido, pero luego de las dos o tres semanas\r\ncomienzan a acompañar a su madre, que se une a otras hembras con crías.</p>
                <h3>DISTRIBUCIÓN</h3>
                <p id="distribution">En Paraguay se distribuye en ambas regiones, pero es escasa en el Chaco Seco.</p>
